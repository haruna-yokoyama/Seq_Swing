package SequenceDiagram;


public class EndPoint {

	private Node node;
	private Node ParentNode;

	public Node getNode() {
		return node;
	}

	public void setNode(Node node) {
		this.node = node;
	}

	public Node getParentNode() {
		return ParentNode;
	}

	public void setParentNode(Node parentNode) {
		this.ParentNode = parentNode;
	}
}
